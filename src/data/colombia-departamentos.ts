/**
 * Obtiene la lista de ciudades de un departamento específico
 */
export const getCiudadesPorDepartamento = (departamento: string): string[] => {
  const dept = DEPARTAMENTOS_COLOMBIA.find(
    (d) => d.nombre.toLowerCase() === departamento.toLowerCase()
  );
  return dept ? dept.ciudades : [];
};

/**
 * Valida si un departamento existe en Colombia
 */
export const esDepartamentoValido = (departamento: string): boolean => {
  return DEPARTAMENTOS_COLOMBIA.some(
    (d) => d.nombre.toLowerCase() === departamento.toLowerCase()
  );
};

/**
 * Valida si una ciudad pertenece a un departamento específico
 */
export const esCiudadValidaEnDepartamento = (
  ciudad: string,
  departamento: string
): boolean => {
  const ciudades = getCiudadesPorDepartamento(departamento);
  return ciudades.some((c) => c.toLowerCase() === ciudad.toLowerCase());
};

